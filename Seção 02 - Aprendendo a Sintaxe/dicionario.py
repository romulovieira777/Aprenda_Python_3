# Key Type Lists: Value - Dictionary (Listas Tipo Chave: Valor)
books = {}

books['Title'] = 'Learn to program in Python in 6hs'  # [Título] Aprenda a programar em Python em 6hs
books['age'] = 2018  # ano

people = {'name': 'José da Silva', 'age': 30, 'height': 1.8, 'weight': 93}  # nome, idade, altura,peso

'''
The dictionary methods are similar to a function, have parameters and return values,
but the syntax is different.

(Os métodos do dicionário são parecidas com uma função, possuem parametros e retornam valores,
mas a sintaxe é diferente).
'''
people.keys()  # Returns the list with the indexes (Retorna a lista com os índices)

people.values()  # Returns the list of values (Retorna a lista com os valores)
